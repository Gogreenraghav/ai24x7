def send_email_report(to_email, report_text):
    """Send daily report via Gmail"""
    from email.mime.multipart import MIMEMultipart
    from email.mime.text import MIMEText
    
    msg = MIMEMultipart()
    msg['to'] = to_email
    msg['from'] = 'arjunsinghlive19@gmail.com'
    msg['subject'] = f"AI24x7 Daily Report - {datetime.now().strftime('%Y-%m-%d')}"
    
    body = MIMEText(report_text, 'plain')
    msg.attach(body)
    
    raw = base64.urlsafe_b64encode(msg.as_bytes()).decode('utf8').replace('=', '')
    
    headers = {"Authorization": f"Bearer {GMAIL_ACCESS}"}
    r = requests.post(
        "https://gmail.googleapis.com/gmail/v1/users/me/messages/send",
        headers=headers,
        json={"raw": raw},
        timeout=15
    )
    return r.status_code == 200

